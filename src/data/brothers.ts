import { pledgeClasses } from "./pledge-classes";

export type Brother = {
  name: string;
  classLetter: string;   // matches a PledgeClass.letter
  className: string;     // matches a PledgeClass.name
  /** Academic major — optional until populated per brother. */
  major?: string;
  detail?: string;
  photo?: string;
};

/** Use a string for name-only rows, or `{ name, major }` when major is known. */
export type RosterEntry = string | { name: string; major?: string };

function rosterName(entry: RosterEntry): string {
  return typeof entry === "string" ? entry : entry.name;
}

function rosterMajor(entry: RosterEntry): string | undefined {
  return typeof entry === "string" ? undefined : entry.major;
}

